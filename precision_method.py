print('{0}'.format(3.14))
print('{0:f}'.format(3.14))

print('{0:d}'.format(3))
print('{0:f}'.format(3.1))

print('%f' %3.14)
print('{0:f}'.format(3.14))

print('%.4f' %3.14)
print('{0:.4f}'.format(3.14))

print('%9.4f' %3.14)
print('{0:9.4f}'.format(3.14))

print('{0:<10.4f}'.format(3.14))
print('{0:>10.4f}'.format(3.14))
print('{0:^10.4f}'.format(3.14))

print('%+d, %+d' %(5,-5))
print('{0:+d},{1:+d}'.format(5,-5))
print('{0:+},{1:+}'.format(5,-5))
print('{:+},{:+}'.format(5,-5))

print('{0:*^10.4f}'.format(3.14))
print('{0:+<10}'.format(7))
print('{0:^^10}'.format('hi'))
